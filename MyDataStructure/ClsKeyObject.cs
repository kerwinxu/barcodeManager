using System;
using System.Collections.Generic;
using System.Text;

namespace Xuhengxiao.MyDataStructure
{
    [Serializable]
    public   class ClsKeyObject
    {
        private object _key;

        public object Key
        {
            get { return _key; }
            set { _key = value; }
        }

        private object _Value;

        public object Value
        {
            get { return _Value; }
            set { _Value = value; }
        }

        public ClsKeyObject(object key, object value)
        {
            Key = key;
            Value = value;

        }

        /// <summary>
        /// 默认的构造函数，用在序列化中
        /// </summary>
        public ClsKeyObject()
        {

        }

    }
}
